//bucle for indicando los numeros ordenados del 1 al 100 y ellos mismos elevados al cubo.
#include <stdio.h>
int main(){
	int i,z;
	for(i=1;i<=100;i++){ //numeros del 1 al 100 elevados al cubo
		z=i*i*i;
		printf("%i al cubo= %i\n",i,z);
	}
	
	
	
	return 0;
	
}


